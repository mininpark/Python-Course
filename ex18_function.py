#1. ALTERNATIVE
def print_two(*args):
    arg1, arg2 = args
    print ("arg1: %r, arg2: %r" % (arg1, arg2))

#2. ALTERNATIVE 
def print_two_again(arg1, arg2):
    print ("arg1: %r, arg2: %r" % (arg1, arg2))

# This one takes only ONE argument 
def print_one(arg1):
    print ("arg1: %r" % arg1)

# This on takes no argument
def print_none():
    print ("I got nothing.")

